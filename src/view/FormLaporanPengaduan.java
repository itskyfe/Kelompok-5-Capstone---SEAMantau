    private void btnTambahPengaduanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahPengaduanActionPerformed
        if (drpJenisPengaduan.getSelectedIndex() <= 0) { 
             JOptionPane.showMessageDialog(this, "Silakan pilih jenis pengaduan!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String jenisString = (String) drpJenisPengaduan.getSelectedItem();
        String deskripsi = txtDeskripsi.getText().trim();
        String koordinat = txtKoordinat.getText().trim();
        
        if (deskripsi.isEmpty() || koordinat.isEmpty()) { 
            JOptionPane.showMessageDialog(this, "Semua field teks harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (selectedImagePath == null || selectedImagePath.isBlank()) {
            JOptionPane.showMessageDialog(this, "Silakan upload foto bukti!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        JenisPengaduan jenisEnum = JenisPengaduan.valueOf(jenisString);

        LaporanPengaduan laporanBaru = new LaporanPengaduan();
        laporanBaru.setUser(this.currentUser); 
        laporanBaru.setJenisPengaduan(jenisEnum);
        laporanBaru.setDeskripsi(deskripsi);
        laporanBaru.setTitikKoordinat(koordinat);
        
        File fotoFile = new File(selectedImagePath);

        try {
            controller.buatLaporanBaru(laporanBaru, fotoFile, DRIVE_FOLDER_ID);
            
            JOptionPane.showMessageDialog(this, "Laporan pengaduan berhasil dikirim!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            
            loadTableData();
            clearForm();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengirim laporan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
//            e.printStackTrace(); 
        }
    }//GEN-LAST:event_btnTambahPengaduanActionPerformed

