    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        // TODO add your handling code here:
          JnaFileChooser fileChooser = new JnaFileChooser();
    
    fileChooser.setTitle("Pilih Foto Kapal");
    
    fileChooser.addFilter("Gambar (*.jpg, *.png, *.jpeg)", "jpg", "png", "jpeg");
    
    boolean result = fileChooser.showOpenDialog(this);

   if (result) { 
            File selectedFile = fileChooser.getSelectedFile();
            
            if (selectedFile != null) {
                
                
                long fileSizeInBytes = selectedFile.length();
                long fileSizeInMB = fileSizeInBytes / (1024 * 1024);
                
                long MAX_FILE_SIZE_MB = 5; 

                if (fileSizeInMB > MAX_FILE_SIZE_MB) {
                    JOptionPane.showMessageDialog(this, 
                        "Ukuran file terlalu besar (" + fileSizeInMB + " MB).\nBatas maksimal adalah " + MAX_FILE_SIZE_MB + " MB.", 
                        "Error Ukuran File", 
                        JOptionPane.ERROR_MESSAGE);
                    
                    selectedImagePath = null;
                    lblPreview.setVisible(false);
                    return; 
                }

                selectedImagePath = selectedFile.getAbsolutePath();
                lblPreview.setText("File terpilih: " + selectedFile.getName());
                lblPreview.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnUploadActionPerformed

    private void btnHapusPengaduanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusPengaduanActionPerformed
           
        int selectedRow = tblPengaduan.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih laporan yang ingin dihapus dari tabel.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        

        if (this.currentLaporanList == null || this.currentLaporanList.isEmpty()) {
             JOptionPane.showMessageDialog(this, "Data laporan tidak ditemukan.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        LaporanPengaduan laporan = this.currentLaporanList.get(selectedRow);
        
        int response = JOptionPane.showConfirmDialog(
                this, 
                "Apakah Anda yakin ingin menghapus pengaduan ini?", 
                "Konfirmasi Hapus", 
                JOptionPane.YES_NO_OPTION, 
                JOptionPane.WARNING_MESSAGE
        );
        
        if (response == JOptionPane.YES_OPTION) {
            try {
                controller.hapusLaporanPengaduan(laporan);
                
                JOptionPane.showMessageDialog(this, "Laporan berhasil dihapus.", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                loadTableData(); 
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus laporan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
//                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_btnHapusPengaduanActionPerformed

