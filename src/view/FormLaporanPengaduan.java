    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) tblPengaduan.getModel();
        model.setRowCount(0);
        this.currentLaporanList = dao.findByUserId(currentUser.getUserId());
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");
        if (this.currentLaporanList != null) { 
            for (LaporanPengaduan lap : this.currentLaporanList) { 
                model.addRow(new Object[]{
                    lap.getTanggalPengaduan().format(formatter),
                    lap.getJenisPengaduan(), 
                    lap.getDeskripsi(),
                    lap.getTitikKoordinat(),
                    lap.getStatusPengaduan(), 
                    lap.getCatatan()
                });
            }
        }
    }
